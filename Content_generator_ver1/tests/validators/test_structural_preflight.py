from content_gen.validators.structural_preflight import StructuralPreflight


def test_structural_preflight_accepts_alternative_intro_headings():
    md = """# Проект

Короткая, но достаточная аннотация для прохождения проверки. Она описывает контекст проекта и ожидаемый результат.

## Содержание

## Глава 1. Введение и инструкция

### Контекст проекта

Это вводный блок проекта, который используется для объяснения основной идеи и того, что решает проект в реальной задаче.
Он задает контекст, объясняет зачем нужен проект и как он применяется в живой практике обучения.

### Требования

**Контекст и ограничения проекта**

Для работы обязательно использовать кампусную среду, локальная подготовка допускается, изменять служебные файлы запрещено.

## Глава 2. Теория

### 2.1. Тема

Текст теории.

## Глава 3. Практика

### Задание 1. Проверка

Текст задачи.
"""

    result = StructuralPreflight(language="ru").check(md)

    hard_messages = [issue.message for issue in result.hard_issues]
    assert "Отсутствует секция «Введение» в Главе 1" not in hard_messages
    assert "Отсутствует секция «Инструкция» в Главе 1" not in hard_messages
