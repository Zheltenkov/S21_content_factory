"""Shared active-goal verb dictionary for practice validators."""

from __future__ import annotations

import re


ACTIVE_GOAL_VERB_PATTERNS: tuple[str, ...] = (
    r"\bразработ(?:ать|ай|айте)\b",
    r"\bнастро(?:ить|й|йте)\b",
    r"\bпровер(?:ить|ь|ьте)\b",
    r"\bсозд(?:ать|ай|айте)\b",
    r"\bреализ(?:овать|уй|уйте)\b",
    r"\bнапис(?:ать|и|ите)\b",
    r"\bпостро(?:ить|й|йте)\b",
    r"\bпротестир(?:овать|уй|уйте)\b",
    r"\bопис(?:ать|и|ите)\b",
    r"\bпроанализир(?:овать|уй|уйте)\b",
    r"\bвыдел(?:ить|и|ите)\b",
    r"\bопредел(?:ить|и|ите)\b",
    r"\bоцен(?:ить|и|ите)\b",
    r"\bпримен(?:ить|и|ите)\b",
    r"\bиспольз(?:овать|уй|уйте)\b",
    r"\bнай(?:ти|ди|дите)\b",
    r"\bидентифицир(?:овать|уй|уйте)\b",
    r"\bклассифицир(?:овать|уй|уйте)\b",
    r"\bзадокументир(?:овать|уй|уйте)\b",
    r"\bсопостав(?:ить|ь|ьте)\b",
    r"\bприоритизир(?:овать|уй|уйте)\b",
    r"\bвыбр(?:ать|ери|ерите)\b",
    r"\bвыполн(?:ить|и|ите)\b",
    r"\bпрове(?:сти|ди|дите)\b",
    r"\bсостав(?:ить|ь|ьте)\b",
    r"\bподготов(?:ить|ь|ьте)\b",
    r"\bсформир(?:овать|уй|уйте)\b",
    r"\b(?:собрать|собери|соберите)\b",
    r"\bорганиз(?:овать|уй|уйте)\b",
    r"\bвнедр(?:ить|и|ите)\b",
    r"\bзапуст(?:ить|и|ите)\b",
)


def has_active_goal_verb(goal_text: str) -> bool:
    """Return True when a goal contains a concrete action verb."""
    return any(re.search(pattern, goal_text or "", flags=re.I) for pattern in ACTIVE_GOAL_VERB_PATTERNS)
