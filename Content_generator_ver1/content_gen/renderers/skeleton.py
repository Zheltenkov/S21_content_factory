"""
content_gen/renderers/skeleton.py

Renderer каркаса документа.

Создает базовую структуру markdown с заголовками глав.
Поддерживает локализацию (ru, en, ky).
"""

from dataclasses import dataclass

LOCALE = {
    "ru": {
        "toc": "## Содержание",
        "ch1": "## Глава 1. Введение и инструкция",
        "ch2": "## Глава 2. Теоретический блок",
        "ch3": "## Глава 3. Практический блок",
        "bonus": "## Бонус",
        "final": "## Заключение",
    },
    "en": {
        "toc": "## Content",
        "ch1": "## Chapter 1. Introduction & Guidelines",
        "ch2": "## Chapter 2. Theory",
        "ch3": "## Chapter 3. Practice",
        "bonus": "## Bonus",
        "final": "## Conclusion",
    },
    "kg": {
        "toc": "## Мазмун",
        "ch1": "## 1-Бөлүм. Киришүү жана эрежелер",
        "ch2": "## 2-Бөлүм. Теория",
        "ch3": "## 3-Бөлүм. Практика",
        "bonus": "## Бонус",
        "final": "## Жыйынтык",
    },
}


@dataclass
class SkeletonParts:
    """Части каркаса документа."""

    toc_anchor: str
    ch1: str
    ch2: str
    ch3: str
    bonus: str | None
    final: str


class SkeletonRenderer:
    """Строит каркас документа с заглушками без LLM-вызовов."""

    def validate_structure(self, skeleton_md: str, language: str, has_bonus: bool) -> tuple[bool, list[str]]:
        """
        Проверяет структуру скелета на соответствие требованиям.
        
        Args:
            skeleton_md: Сгенерированный скелет документа
            language: Язык проекта
            has_bonus: Есть ли бонусная задача
            
        Returns:
            (is_valid, issues) - кортеж из флага валидности и списка проблем
        """
        import re
        issues = []
        L = LOCALE.get(language, LOCALE["ru"])

        # Проверка наличия обязательных глав
        required_chapters = [
            (L['toc'], "Содержание/Оглавление"),
            (L['ch1'], "Глава 1. Введение и инструкция"),
            (L['ch2'], "Глава 2. Теоретический блок"),
            (L['ch3'], "Глава 3. Практический блок"),
        ]

        for chapter_marker, chapter_name in required_chapters:
            # Ищем заголовок (может быть с разным количеством #)
            pattern = re.compile(rf'^#+\s*{re.escape(chapter_marker.replace("## ", ""))}', re.MULTILINE | re.IGNORECASE)
            if not pattern.search(skeleton_md):
                issues.append(f"Отсутствует обязательный раздел: {chapter_name}")

        # Проверка бонуса (если должен быть)
        if has_bonus:
            bonus_pattern = re.compile(rf'^#+\s*{re.escape(L["bonus"].replace("## ", ""))}', re.MULTILINE | re.IGNORECASE)
            if not bonus_pattern.search(skeleton_md):
                issues.append(f"Отсутствует раздел: {L['bonus']}")

        # Проверка структуры Главы 1 (должны быть подразделы Введение и Инструкция)
        if language == "ru":
            if "### Введение" not in skeleton_md and "## Введение" not in skeleton_md:
                issues.append("В Главе 1 отсутствует подраздел 'Введение'")
            if "### Инструкция" not in skeleton_md and "## Инструкция" not in skeleton_md:
                issues.append("В Главе 1 отсутствует подраздел 'Инструкция'")

        # Проверка наличия H1 заголовка
        h1_pattern = re.compile(r'^#\s+.+$', re.MULTILINE)
        if not h1_pattern.search(skeleton_md):
            issues.append("Отсутствует заголовок H1 (название проекта)")

        is_valid = len(issues) == 0
        return is_valid, issues

    def build(self, language: str, has_bonus: bool) -> SkeletonParts:
        """
        Создаёт каркас документа.

        Args:
            language: Язык проекта
            has_bonus: Есть ли бонусная задача

        Returns:
            SkeletonParts с частями каркаса
        """
        L = LOCALE.get(language, LOCALE["ru"])
        toc = f"{L['toc']}\n\n<!-- TOC_PLACEHOLDER -->\n"
        ch1 = (
            f"{L['ch1']}\n\n"
            f"<!-- \n"
            f"СТРУКТУРА ГЛАВЫ 1 (ВВЕДЕНИЕ И ИНСТРУКЦИЯ):\n"
            f"- ### Введение: 80-250 слов\n"
            f"  * Объясняет идею проекта и контекст задачи\n"
            f"  * Упоминает реальный контекст применения (маркеры: 'используется для', 'в реальной задаче', 'применяется')\n"
            f"  * Объясняет 'зачем/что решает/основная идея'\n"
            f"- ### Инструкция: рамки и правила на весь проект\n"
            f"  * ОБЯЗАТЕЛЬНО в формате пунктов (bullets)\n"
            f"  * ОБЯЗАТЕЛЬНО содержит слова: 'допускается', 'запрещено', 'обязательно'\n"
            f"  * Указывает обязательные инструменты\n"
            f"  * НЕ содержит пошаговых действий ('нажми/кликни/перейди')\n"
            f"-->\n\n"
            f"### Введение\n\n(здесь будет 80–250 слов)\n\n"
            f"### Инструкция\n\n(глобальные рамки, «допускается/запрещено/обязательно», инструменты)\n"
        )
        ch2 = f"""{L['ch2']}

<!-- 
СТРУКТУРА ГЛАВЫ 2 (ТЕОРЕТИЧЕСКИЙ БЛОК):
- Должно быть 2-5 частей (### 2.1. название раздела, ### 2.2. название раздела, и т.д.)
- Каждая часть: 100-300 слов основного текста
- ОБЯЗАТЕЛЬНО в каждой части:
  1. Минимум 2 явных определения терминов в основном тексте (через "<термин> — это <определение>" или "Определение <термина>: <определение>")
  2. Блок **Пример:** (1-3 предложения, короткий кейс из реальности)
  3. Блок **Вопросы к практике:** (минимум 1 вопрос, связывающий теорию с практикой)
- Определения должны быть естественно вплетены в текст, не в конце
- Каждое определение должно быть полным предложением с точкой
- ВАЖНО: Используй формат размышлений, контекста, историй из практики, а не прямое изложение знаний
-->

### 2.1. <название раздела>

<основной текст, 100–300 слов, с явными определениями терминов через конструкции: "<термин> — это <определение>" или "Определение <термина>: <определение>">

**Пример:** <1–3 предложения, короткий кейс из реальности>

**Вопросы к практике:**
- <вопрос 1, связывающий теорию с практикой>
- <вопрос 2 (опционально)>
"""
        ch3 = (
            f"{L['ch3']}\n\n"
            f"<!-- \n"
            f"СТРУКТУРА ГЛАВЫ 3 (ПРАКТИЧЕСКИЙ БЛОК):\n"
            f"- Должно быть 3-8 заданий (### Задание 1. название, ### Задание 2. название, и т.д.)\n"
            f"- Каждое задание ОБЯЗАТЕЛЬНО содержит публичные блоки:\n"
            f"  1. **Что нужно сделать**\n"
            f"  2. **Что должно получиться**\n"
            f"  3. **Ограничения и условия** (если есть ограничения/риски)\n"
            f"  4. **Формат сдачи**\n"
            f"  5. **Переход к следующему заданию**\n"
            f"-->\n\n"
            f"### Задание 1. <название>\n\n"
            f"**Что нужно сделать**\n\nСитуация: …\n\nИсходные данные: …\n\nЦель: …\n\nПодход:\n- …\n\n"
            f"**Что должно получиться**\n\n- [ ] …\n- [ ] …\n\n"
            f"**Формат сдачи**\n\nНа p2p-ревью покажи артефакт по указанному пути.\n\n"
            f"**Переход к следующему заданию**\n\n…\n"
        )
        bonus = f"{L['bonus']}\n\n(опционально: цель-вызов и артефакт)\n" if has_bonus else None
        final = f"{L['final']}\n\n(финальное завершение текущего проекта без анонса следующего)\n"
        return SkeletonParts(toc_anchor=toc, ch1=ch1, ch2=ch2, ch3=ch3, bonus=bonus, final=final)

    def stitch(self, title: str, annotation_md: str, sk: SkeletonParts) -> str:
        """
        Собирает полный каркас документа.

        Args:
            title: Заголовок H1
            annotation_md: Текст аннотации
            sk: Части каркаса

        Returns:
            Полный Markdown с каркасом
        """
        parts = [
            f"# {title}\n",
            f"{annotation_md}\n\n",
            sk.toc_anchor,
            sk.ch1,
            "\n",
            sk.ch2,
            "\n",
            sk.ch3,
        ]
        if sk.bonus:
            parts += ["\n", sk.bonus]
        parts += ["\n", sk.final]
        return "".join(parts).rstrip() + "\n"


# Backward-compatible alias for older imports. New code should use SkeletonRenderer.
SkeletonAgent = SkeletonRenderer
